export const MS_PER_SECOND: number = 1000;
export const MS_PER_MINUTE: number = 60 * MS_PER_SECOND;
export const MS_PER_HOUR: number = 60 * MS_PER_MINUTE;
export const MS_PER_DAY: number = 24 * MS_PER_HOUR;
export const MS_PER_WEEK: number = 7 * MS_PER_DAY;
export const MS_PER_YEAR: number = 365.25 * MS_PER_DAY; // Average, accounting for leap years

export const BYTES_PER_KB: number = 1000;
export const BYTES_PER_MB: number = 1000 * BYTES_PER_KB;
export const BYTES_PER_GB: number = 1000 * BYTES_PER_MB;

export const BYTES_PER_KIB: number = 1024;
export const BYTES_PER_MIB: number = 1024 * BYTES_PER_KIB;
export const BYTES_PER_GIB: number = 1024 * BYTES_PER_MIB;
